    private String Host = "127.0.0.1";
    private DataOutputStream salida;
    private String nombre;
    private hilocliente hilo;

    public Cliente() {
        initComponents();
        try {
            nombre = JOptionPane.showInputDialog("Su nombre ");
            super.setTitle(super.getTitle() + nombre);
            super.setVisible(true);
            cliente = new Socket(Host, puertoc); // envia conexion al servidor, puerto conexion
            salida = new DataOutputStream(cliente.getOutputStream());
            salida.writeUTF(nombre);
            hilo = new hilocliente(cliente, this);
            hilo.start();

        } catch (HeadlessException | IOException e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }

