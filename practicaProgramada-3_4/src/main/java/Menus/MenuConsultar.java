    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_buscarBtnActionPerformed
        List<Venta> ventas = ArchivoVentas.LeerDatosDeArchivo();
        if (cedulaBuscar.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Cedula vacia.", getTitle(), 0);
            return;
        }
        for (Venta venta : ventas) {
            if (venta.getCedula().equals(cedulaBuscar.getText())) {
                DefaultTableModel modelo = new DefaultTableModel();
                List<Productos> productos = venta.getProductos();
                modelo.addColumn("Nombre");
                modelo.addColumn("Tipo");
                modelo.addColumn("Marca");
                modelo.addColumn("Precio");
                modelo.addColumn("Cantidad");
                modelo.addColumn("Total");
                for (Productos productos2 : productos) {
                    modelo.addRow(new Object[] { productos2.getNombre(), productos2.getTipo(), productos2.getMarca(),
                            productos2.getPrecio(), productos2.getCantidad() });
                }
                modelo.addRow(new Object[] { null, null, null, null, "Total: ", venta.getTotal() });
                jTable1.setModel(modelo);
                return;
            }
        }

        JOptionPane.showMessageDialog(rootPane, "Cedula inexistente.", getTitle(), 0);

    }// GEN-LAST:event_buscarBtnActionPerformed

