    private void registrarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_registrarBtnActionPerformed
        List<String> marcas = ArchivoMarcas.LeerDatosDeArchivo();
        for (String string : marcas) {
            if (string.equals(marcaTxtFld.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Ya existe una marca con ese nombre.", getTitle(), 0);
                return;
            }
        }
        if (marcaTxtFld.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Nombre vacio.", getTitle(), 0);
            return;
        } else {
            String marca = marcaTxtFld.getText();
            ArchivoMarcas archivo = new ArchivoMarcas();
            archivo.EscribirEnArchivo(marca);
            JOptionPane.showMessageDialog(rootPane, "Marca registrada.", "Marca", 1);
            dispose();
        }
    }// GEN-LAST:event_registrarBtnActionPerformed

