    private void enviarPlanillaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_enviarPlanillaActionPerformed
        Socket socket = null;
        ObjectOutputStream stream = null;
        List<Colaborador> personas = new ArrayList<>();
        Planilla planilla = new Planilla();
        try {
            socket = new Socket("localhost", 3302);
            stream = new ObjectOutputStream(socket.getOutputStream());
            for (Colaborador colaborador : colaboradores) {
                if (colaborador.getFecha().toString().equals(fechasRegistradas.getSelectedItem())) {
                    personas.add(colaborador);
                }
            }
            planilla.setColaboradores(personas);
            stream.writeObject(planilla);
            stream.close();
            socket.close();
        } catch (UnknownHostException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }// GEN-LAST:event_enviarPlanillaActionPerformed

