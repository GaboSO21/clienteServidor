    public void actualizarLista() {
        try {
            colaboradores = conexionSQL.select();
            conexion.commit();
        } catch (SQLException e) {
            e.printStackTrace();
            try {
                conexion.rollback();
            } catch (SQLException e1) {
                e1.printStackTrace();
            }

        }
    }

    public void actualizarSet() {
        for (Colaborador colaborador : colaboradores) {
            fechas.add(colaborador.getFecha());
        }
    }

